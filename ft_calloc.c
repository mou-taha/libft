#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void			*res;
	size_t			ttl_bytes;

	ttl_bytes = nmemb * size;
	res = malloc(ttl_bytes);
	if (!res)
		return (NULL);
	res = ft_memset(res, 0, ttl_bytes);
	return (res);
}
